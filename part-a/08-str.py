s1 = "hello world!"

print("upper: " + s1.upper())
print("lower: " + s1.lower())
print("capitalize: " + s1.capitalize())
print("count: " + str(s1.count('e')))
print("isdigit: " + str(s1.isdigit()))
print("isupper: " + str(s1.isupper()))
print("islower: " + str(s1.islower()))
print("replace o with i: " + s1.replace('o', 'i'))
print("title: " + s1.title())
print("split:", s1.split())

